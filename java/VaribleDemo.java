public class VaribleDemo {
    public static void main(String[] args){
        int a = 10;
        System.out.print(a);
        a = 20;
        System.out.print('\n');
        System.out.print(a);
        long l = 100000000000000L;
        System.out.print('\n');
        System.out.print(l);
        float f = 3.1415f;
        System.out.print('\n');
        System.out.print(f);
    }
    
}